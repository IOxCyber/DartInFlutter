main() {
  var operand1 = 10;
  var operand2 = 7;

  print(operand1 + operand2); //17
  print(operand1 - operand2); //3
  print(- operand1); //10
  print(operand1 * operand2);//70
  print(operand1 / operand2);//1.4285714285714286
  //Divides the first operand by the second operand and returns an integer value
  print(operand1 ~/ operand2);//1
  print(operand1 % operand2);//3
}
